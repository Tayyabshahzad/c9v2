                <p class="pb-50">
                    Amygdala XDR File Integrity monitoring uses an agent-based approach to monitor the file system. The
                    agent runs on the target system and periodically scans the file system for changes. The agent can be
                    configured to monitor specific directories, files, or file attributes. It collects metadata about
                    each file it monitors, such as file size, timestamps, permissions, and ownership. The Amygdala XDR File
                    Integrity monitoring also calculates a hash value for each file, which is a unique identifier based
                    on the file content. Overall, Amygdala XDR's FIM module detects and responds to changes to critical files and system objects
                    on your servers and workstations. It not only provides real-time alerts but also furnishes
                    historical data that can be utilized for scrutinizing past events and monitoring modifications over
