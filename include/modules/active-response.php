                <h3 class="text-capitalize mb-20">Technical Description </h3>
                <p class="mb-10">
                    When a security event is detected by the Amygdala XDR agent, an alert is generated and sent to the
                    Amygdala XDR server for processing. The Amygdala XDR server receives the alert and evaluates its severity
                    and relevance. Based on pre-defined rules and policies, the server determines whether an active
                    response is required. If an active response is required, the Amygdala XDR server triggers the
                    appropriate response action, it includes blocking IP addresses, terminating processes, quarantining
                    files, or running scripts.
                </p>
                <p class="pb-50">
                    The response action is executed on the affected system(s). For example, if the response action is to
                    block an IP address, the Amygdala XDR server sends a command to the firewall or network device to block
                    traffic from the offending IP address. The Amygdala XDR server logs the response action and sends
                    notifications to the appropriate administrators. It provides a record of the response to the
                    security event and allows administrators to review and verify the effectiveness of the response.
                </p>

